package prob01;

public class Printer {

	// 오버로드
	public void println(int data) {
		System.out.println(data);
	}
	public void println(boolean data) {
		System.out.println(data);
	}
	public void println(double data) {
		System.out.println(data);
	}
	public void println(String data) {
		System.out.println(data);
	}
}
