package chapter04;

// Line 처리
public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\temp");
		
		// "HELLO"
		System.out.println("\"HELLO\"");
		
		// \t : tab
		// \r : carriage return
		// \n : new line
		// \b : bell
		System.out.print("hello\tword\n");
		System.out.println("hello\tword");
		
		// '
		char c = '\'';
		System.out.println(c);
		
	}

}
